from __future__ import annotations

from collections.abc import Mapping
from typing import Any, TypeVar

from attrs import define as _attrs_define

from ..types import UNSET, Unset

T = TypeVar("T", bound="VeeamVaultSignInResult")


@_attrs_define
class VeeamVaultSignInResult:
    """
    Attributes:
        token (str | Unset): Token generated by Veeam Backup for Microsoft Azure that you can use to register your
            backup appliance in Veeam Data Cloud Vault.
    """

    token: str | Unset = UNSET

    def to_dict(self) -> dict[str, Any]:
        token = self.token

        field_dict: dict[str, Any] = {}

        field_dict.update({})
        if token is not UNSET:
            field_dict["token"] = token

        return field_dict

    @classmethod
    def from_dict(cls: type[T], src_dict: Mapping[str, Any]) -> T:
        d = dict(src_dict)
        token = d.pop("token", UNSET)

        veeam_vault_sign_in_result = cls(
            token=token,
        )

        return veeam_vault_sign_in_result
